def get_district_contact(district_name: str) -> str:
    """
    자치구 이름으로 연락처 정보를 반환
    
    Args:
        district_name: 자치구 이름 (예: "종로구", "강남구")
    
    Returns:
        포맷된 연락처 정보 문자열
    """
    # 공백 제거 및 '구' 자동 추가
    district_name = district_name.strip()
    if not district_name.endswith("구"):
        district_name += "구"
    
    if district_name in DISTRICT_CONTACTS:
        info = DISTRICT_CONTACTS[district_name]
        return f"""
### 📞 {district_name} 연락처

**{info['부서']}**: ☎ {info['연락처']}

{GENERAL_CONTACTS}

💡 위 연락처로 전화하시면 담당자가 자세히 안내해드립니다.
"""
    else:
        return f"""
### 📞 연락처 안내

**{district_name}** 정보가 등록되어 있지 않습니다.
아래 통합 상담센터로 문의해주세요.

{GENERAL_CONTACTS}
"""